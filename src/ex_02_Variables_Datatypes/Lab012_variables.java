package ex_02_Variables_Datatypes;

public class Lab012_variables {
    static void main(String[] args) {
        System.out.println("variables");
        // Syntax: data_type var_name = var_value ;
        byte age = 28;
        // byte is data_type
        // age is variable name or identifier
        //28 is var_value or literal
        System.out.println(age);
        int akash=30;
        System.out.println("akash:" +akash);
    }
}
