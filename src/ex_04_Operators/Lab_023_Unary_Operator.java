package ex_04_Operators;

public class Lab_023_Unary_Operator {
    static void main(String[] args) {
        // unary operator
        int a = +20;
        int b = 20; // by default positive
        System.out.println(a);

        int a1 = -110;
        int result = a+a1;
        System.out.println(result);

        int c = -1;
        b = b+1;
        System.out.println(b);
    }
}
