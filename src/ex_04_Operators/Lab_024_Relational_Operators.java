package ex_04_Operators;

public class Lab_024_Relational_Operators {
    static void main(String[] args) {
        int a = 10;
        int b = 30;
        boolean c = a > b; // // 10> 30
        System.out.println(c);

        // < Less Than
        // <=  -> Less than or equal to
        // > Greater
        // >= Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal
    }
}
