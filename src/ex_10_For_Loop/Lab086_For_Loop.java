package ex_10_For_Loop;

public class Lab086_For_Loop {
    static void main(String[] args) {
        // For Loop
        // Help to repeat a block of code.
        // Initialization -> Condition -> Updation (Incre/decre) (ICU)
        for (int i=0; i<10; i++)
            System.out.println(i);
    }
}
